<p>This access is only provided for the following use cases:</p>
<ul>
  <li>Evaluating or improving the quality of digital files or catalog records in the HathiTrust collection</li>
  <li>Reviewing the copyright status of HathiTrust volumes</li>
  <li>Developing or maintaining HathiTrust systems, applications, and the repository</li>
</ul>
<p>
  Individuals making use of Collection Administration Access are not permitted to use this access for other use cases,
  such as:
</p>
<ul>
  <li>Answering patron reference questions using copyrighted HathiTrust books.</li>
  <li>Reading copyrighted HathiTrust books for their own purposes.</li>
  <li>Downloading or otherwise copying and sharing copyrighted files for personal use.</li>
  <li>Downloading or otherwise copying and sharing copyrighted files with patrons.</li>
  <li>
    Downloading and sharing pages from copyrighted works with co-workers who will use them for any non-permitted uses
    listed above. However, to meet the permitted use cases above, users with Collection Administration Access may, when
    absolutely necessary, download and share a few pages of a copyrighted work with co-workers.
  </li>
  <li>Providing others with credentials to use Collection Administration Access to perform any task.</li>
</ul>
